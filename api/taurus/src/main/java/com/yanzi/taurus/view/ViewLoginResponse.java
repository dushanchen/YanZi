package com.yanzi.taurus.view;

public class ViewLoginResponse {
    private long id;
    private String token;

    public ViewLoginResponse(long id, String token) {
        this.id = id;
        this.token = token;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
