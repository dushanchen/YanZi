package com.yanzi.pisces.entity;

public class UserCollegeStatus {
    private long exp;
    private long knowledge;
    private int rank;

    public long getExp() {
        return exp;
    }

    public void setExp(long exp) {
        this.exp = exp;
    }

    public long getKnowledge() {
        return knowledge;
    }

    public void setKnowledge(long knowledge) {
        this.knowledge = knowledge;
    }

    public int getRank() {
        return rank;
    }

    public void setRank(int rank) {
        this.rank = rank;
    }
}
